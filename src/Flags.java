public class Flags {

    public boolean o;
    public boolean p;
    public boolean s;
    public boolean f;
    public boolean a;

    public Flags(boolean o, boolean p, boolean s, boolean f, boolean a) {
        this.o = o;
        this.p = p;
        this.s = s;
        this.f = f;
        this.a = a;
    }

}
